    query = input("Please enter the message you want to to translate: ")
    if query == "quit":
        if input("Are you sure? (y/n): ") == "y":
            raise SystemExit("Exit program")
        else: pass
    print(query)
    query_split = split(query)
    for cipher in query_split:
        if cipher in alphabet:
            message += alphabet[cipher]
        else:
            message += "/    "
    print(f"Your morse code: {message} \nType 'quit' to exit")

